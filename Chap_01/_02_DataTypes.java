package Chap_01;

public class _02_DataTypes {

public static void main(String[] args) {
    System.out.println("Hello, World"); /*문자열*/
    System.out.println("안녕하세요?"); /*문자열*/
    System.out.println(12); /*숫자 자료형 : 정수*/
    System.out.println(-34); /*숫자 자료형 : 정수*/
    System.out.println(3.14); /*숫자 자료형 : 실수*/
    System.out.println(true); /*불리안 자료형*/
    System.out.println(false); /*불리안 자료형*/
}
    
}

/* psvm 을 작성하면, public static void main(String[] args) {} */
/* sout 을 작성하면, System.out,println(); */